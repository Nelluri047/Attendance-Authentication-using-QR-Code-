package app.attendance.data.models;

public class User {
    private String id;
    private String email;
    private String fullName;
    private String studentId;

    public String getId() {
        return id;
    }

    public String getFullName() {
        return fullName;
    }

    public String getEmail() {
        return email;
    }

    public String getStudentId() {
        return studentId;
    }
}
